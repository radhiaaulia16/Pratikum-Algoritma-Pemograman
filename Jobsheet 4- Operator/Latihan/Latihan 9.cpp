#include <stdio.h>

main(void){
	int a = 4;
	
	//increment a
	a++;
	
	printf("a++ = %d\n", a);
	
	//increment a
	++a;
	
	printf("++a = %d\n", a);
	
	//decrement a
	a--;
	
	printf("a-- = %d\n", a);
	
	//decrement a
	--a;
	
	printf("--a = %d\n", a);
}
